CREATE PROCEDURE [BS].[spCheckDuplicateSCI]
AS
SELECT ORGID,YEAR,PERIOD,PDATE,BATCHREF,TRANSREF,CREDNO,CREDIT,ORDERNO,COUNT(*) COUNTS
FROM TRANSACTIONS WHERE LEFT(BATCHREF,3)='SCI' AND YEAR=2020 AND LEDGERCODE='1320000' AND PERIOD<>0
GROUP BY ORGID,YEAR,PERIOD,PDATE,BATCHREF,TRANSREF,CREDNO,CREDIT,ORDERNO
HAVING COUNT(*)>1
ORDER BY  PERIOD 